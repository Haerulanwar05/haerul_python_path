
import random

result_uniform = random.uniform(0.0,5.0) # menghasilkan sebuah float dari rentang 0.0 - 5.0
print(result_uniform)

result_randint = random.randint(1,80) # menghasilkan sebuah integer dari rentang 1 - 80
print(result_randint)

result_random = random.random() # menghasilkan sebuah float dengann rentang otomatis dari 0.0 - 1.0
print(result_random)

