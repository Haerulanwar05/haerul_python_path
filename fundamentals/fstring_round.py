# Pembulatan(Round)
# Pembulatan dilakukan dalam hasil pembagian atau jenis data float 
print(10/3)
print(round(10/3))  # Dalam Pembulatan
print(round(10/3,2))  # Dalam Pembulatan dan ingin mengambil 2 angka dibelakang koma
